	   
	   ORG 0000
START: MOV P2,#00  // P2.0 =0 (LED off)
	   LCALL DELAY1
	   MOV P2,#0FFH // P2.0 =1 (LED ON)
	   LCALL DELAY1
       SJMP START
	   


DELAY1:MOV R0,#0FH
LOOP2: MOV R1,#0FH
LOOP1: DJNZ R1,LOOP1 //DECREMENT AND JUMP ON NO ZERO
	   DJNZ R0,LOOP2
	   RET
	   END 
	   
		